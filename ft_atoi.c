#include "libft.h"

int		ft_atoi(const char *s)
{
	int i;
	int decimal;
	int sign;

	i = 0;
	decimal = 0;
	sign = 1;
	while (s[i] && (s[i] == '\t' || s[i] == '\n' || s[i] == '\v'
		|| s[i] == '\f' || s[i] == '\r' || s[i] == ' '))
		i++;
	if (s[i] == '-')
		sign = -1;
	if (s[i] == '-' || s[i] == '+')
		i++;
	while (s[i] && s[i] >= '0' && s[i] <= '9')
	{
		decimal = decimal * 10 + s[i] - '0';
		i++;
	}
	return (decimal * sign);
}
